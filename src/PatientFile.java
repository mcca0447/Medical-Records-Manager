
public class PatientFile {
	
	String cid, firstName, lastName, address, postalCode, ohipNum, phone, email;
	
	public PatientFile(String cid, String firstName, String lastName,  String address,  String postalCode, String ohipNum, String phone, String email) {
		
		cid = this.cid;
		firstName = this.firstName;
		lastName = this.lastName;
		address = this.address;
		postalCode = this.postalCode;
		ohipNum =  this.ohipNum;
		phone = this.phone;
		email = this.email;
	}
	
	
	
	
	
	

}
